class Solution {
  public:
    int binarysearch(int arr[], int n, int k) {
        int high =n-1;
        int low= 0;
        int mid =0;
       while (low <= high) {
         mid = low + (high - low) / 2;

        // Check if x is present at mid
        if (arr[mid] == k)
            return mid;

        // If x greater, ignore left half
        if (arr[mid] < k)
            low = mid + 1;

        // If x is smaller, ignore right half
        else
            high = mid - 1;
    }

    // If we reach here, then element was not present
    return -1;
}
};

